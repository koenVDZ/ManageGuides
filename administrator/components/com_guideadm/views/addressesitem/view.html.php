/**
* HTML View class for the Guideadm component
*
* @package	Guideadm
* @subpackage	Addressesitem
*/
class GuideadmCkViewAddressesitem extends GuideadmClassView
{
	/**
	* List of the reachables layouts. Fill this array in every view file.
	*
	* @var array
	*/
	protected $layouts = array('usersadress');

	/**
	* Execute and display a template : User\'s Address
	*
	* @access	protected
	* @param	string	$tpl	The name of the template file to parse; automatically searches through the template paths.
	*
	*
	* @since	11.1
	*
	* @return	mixed	A string if successful, otherwise a JError object.
	*/
	protected function displayUsersadress($tpl = null)
	{
		// Initialiase variables.
		$this->model	= $model	= $this->getModel();
		$this->state	= $state	= $this->get('State');
		$this->params 	= $state->get('params');
		$state->set('context', 'layout.usersadress');
		$this->item		= $item		= $this->get('Item');

		$this->form		= $form		= $this->get('Form');
		$this->canDo	= $canDo	= GuideadmHelper::getActions($model->getId());
		$lists = array();
		$this->lists = &$lists;

		// Define the title
		$this->_prepareDocument(JText::_('GUIDEADM_LAYOUT_USERS_ADDRESS'), $this->item, 'user_id');

		$user		= JFactory::getUser();
		$isNew		= ($model->getId() == 0);

		//Check ACL before opening the form (prevent from direct access)
		if (!$model->canEdit($item, true))
			$model->setError(JText::_('JERROR_ALERTNOAUTHOR'));

		// Check for errors.
		if (count($errors = $model->getErrors()))
		{
			JError::raiseError(500, implode(BR, array_unique($errors)));
			return false;
		}
		//Toolbar
		JToolBarHelper::title(JText::_('GUIDEADM_LAYOUT_USERS_ADDRESS'), 'pencil-2');

		// Save & Close
		if (($isNew && $model->canCreate()) || (!$isNew && $item->params->get('access-edit')))
			JToolBarHelper::save('addressesitem.save', "GUIDEADM_JTOOLBAR_SAVE_CLOSE");
		// Save
		if (($isNew && $model->canCreate()) || (!$isNew && $item->params->get('access-edit')))
			JToolBarHelper::apply('addressesitem.apply', "GUIDEADM_JTOOLBAR_SAVE");
		// Cancel
		JToolBarHelper::cancel('addressesitem.cancel', "GUIDEADM_JTOOLBAR_CANCEL");
		// Publish
		if (!$isNew && $model->canEditState($item) && ($item->published != 1))
			JToolBarHelper::publish('addresses.publish', "GUIDEADM_JTOOLBAR_PUBLISH");
		// Unpublish
		if (!$isNew && $model->canEditState($item) && ($item->published != 0))
			JToolBarHelper::unpublish('addresses.unpublish', "GUIDEADM_JTOOLBAR_UNPUBLISH");
		// Trash
		if (!$isNew && $model->canEditState($item) && ($item->published != -2))
			JToolBarHelper::trash('addresses.trash', "GUIDEADM_JTOOLBAR_TRASH", false);
		// Archive
		if (!$isNew && $model->canEditState($item) && ($item->published != 2))
			JToolBarHelper::custom('addresses.archive', 'archive', 'archive',  "GUIDEADM_JTOOLBAR_ARCHIVE", false);


		$model_catid = CkJModel::getInstance('Categories', 'GuideadmModel');
		$model_catid->addGroupOrder("a.MGcat");
		$lists['fk']['catid'] = $model_catid->getItems();

		$model_user_id = CkJModel::getInstance('Contacts', 'GuideadmModel');
		$model_user_id->addGroupOrder("a.name");
		$lists['fk']['user_id'] = $model_user_id->getItems();

		$model_address_label = CkJModel::getInstance('Addresslabels', 'GuideadmModel');
		$model_address_label->addGroupOrder("a.type");
		$lists['fk']['address_label'] = $model_address_label->getItems();

		$model_country_id = CkJModel::getInstance('Countries', 'GuideadmModel');
		$model_country_id->addGroupOrder("a.country_name");
		$lists['fk']['country_id'] = $model_country_id->getItems();

		$model_state_id = CkJModel::getInstance('States', 'GuideadmModel');
		$model_state_id->addGroupOrder("a.state");
		$lists['fk']['state_id'] = $model_state_id->getItems();

		$model_created_by = CkJModel::getInstance('ThirdUsers', 'GuideadmModel');
		$model_created_by->addGroupOrder("a.name");
		$lists['fk']['created_by'] = $model_created_by->getItems();

		$model_modified_by = CkJModel::getInstance('ThirdUsers', 'GuideadmModel');
		$model_modified_by->addGroupOrder("a.name");
		$lists['fk']['modified_by'] = $model_modified_by->getItems();

		//Ordering
		$orderModel = CkJModel::getInstance('Addresses', 'GuideadmModel');
		if (isset($item->user_id))
			$orderModel->addWhere("a.user_id = '" . $item->user_id . "'");
		$lists["ordering"] = $orderModel->getItems();
	}


}

